// In JavaScript, a variable is a container for storing data values.
// You can declare a variable using the var, let, or const keywords.


// let: Block-scoped, can be re-assigned but not re-declared within the same scope.

let a = 5
console.log(a) // 5
let name = "Kartik" // kartik
console.log(name);
let nameLast = "Chauhan" // chauhan
console.log(nameLast);

// var: Function-scoped or globally-scoped, can be re-assigned and re-declared.
var b = 10
console.log(b) // 10;

// const: Block-scoped, cannot be re-assigned or re-declared
const c = 23
console.log(c); // 23
//const c = "Kartik"
console.log(c); // error


